//클래스 생성
class MaskStore {
  final String name;
  final String address;
  final String distance;
  final String remainStatus;

  //생성자 생성(Named Argument Constructor로 만듬)
  const MaskStore({
    required this.name,
    required this.address,
    required this.distance,
    required this.remainStatus,
  });
}